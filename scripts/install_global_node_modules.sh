# DESCRIPTION
# Installs all the nodejs (npm / node) modules I commonly use.

# USAGE
# Run without any parameters:
#    install_global_node_modules.sh
# NOTES
# To get a list of npm packages (without all their dependencies) and paste it into this list, run:
#    npm list -g --depth=0 > installedNPMpackages.txt


# CODE
packagesList=(
ttab
svgo
buzzphrase
csv-to-md
markdown-styles
gh
afterwriting
culori
commander
vanity-eth
jimp
color-thief-jimp
)

# Other potentially used packages:
# d3-cam03
# ungit

# previously used, until I figured out I can require() newer versions this way -- `culori = require('culori/require');` :
# culori@0.20.1

# NOTES
# - markdown-styles (I think!) changed name to generate-md and then back. What on earth, nodejs-land? Also, markdown-styles has many dependency and other warnings on install.

for element in ${packagesList[@]}
do
  npm install -g $element
done

# path is %AppData%\npm\node_modules
if [ $OS == "Windows_NT" ]
then
	printf "DONE. It appears that you're running Windows. You may need to add a NODE_PATH user environment variable if you don't have one set already, with a value appropriate to your platform (probably %%AppData%%\\\\npm\\\\node_modules). You can run the command 'setx NODE_PATH %%AppData%%\\\\npm\\\\node_modules' (without the quote marks) from an administrator command prompt to do this. Or, if you want to set a user (instead of system) environment variable, and you have the _ebPathMan repository or the setenv utility, you can set that with this command, from an administrator command prompt: setenv -u NODE_PATH %%AppData%%\\\\npm\\\\node_modules"
fi